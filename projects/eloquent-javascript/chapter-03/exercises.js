function min(num1, num2) {

  if(num1 < num2){
    for(let i = num1; i <= num2; i++){
    for(let x = num2; x >= num1; x-- ){
      if(i === x){
        return i; 
      }
      }
    }
 } else if(num1 > num2){
  for(let i = num2; i <= num1; i++){
    for(let x = num1; x >= num2; x-- ){
      if(i === x){
        return i; 
      }
      }
 }
  } else if(num1 === num2){
    return num1; 
  }
}


